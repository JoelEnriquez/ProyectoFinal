    private void checkjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkjButtonActionPerformed
        if (nombreEstacionjTextField.getText().isEmpty() || colorEstacion==null) {
            crearTablero.mensajeError("Por favor, complete ambos campos");
        }
        else{
            String nombreEstacion = nombreEstacionjTextField.getText().trim();
            Color color = colorActualjPanel.getBackground();
            
            Estacion nuevaEstacion = new Estacion(fila, columna,nombreEstacion,
                    costoPorEstacion, porcentajeHipoteca , usoEstacion, color);
            
            setEstacion(nuevaEstacion);
            setColorEstacion(color);
            
            this.setVisible(false);     
        }
    }//GEN-LAST:event_checkjButtonActionPerformed

