    private void aceptarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarjButtonActionPerformed
        if (nombreServiciojTextField.getText().isEmpty() || colorServicio==null) {
            crearTablero.mensajeError("Por favor ingrese todos los campos requeridos");
        }
        else{
            String nombreServicio = nombreServiciojTextField.getText().trim();
            int precioCompra = (Integer) precioComprajSpinner.getValue();
            int costoUsoServicio = (Integer) costoUsoServiciojSpinner.getValue();
            Color color = colorSeleccionadoServiciojPanel.getBackground();
            
            ServicioBasico nuevoServicio = new ServicioBasico(fila, columna, nombreServicio, precioCompra,
                    porcentajeHipoteca, costoUsoServicio, color);
            
            setServicioBasico(nuevoServicio);
            setColorServicio(color);
            setNombreServicio(nombreServicio);
            
            this.setVisible(false);
            
        }
        
    }//GEN-LAST:event_aceptarjButtonActionPerformed

